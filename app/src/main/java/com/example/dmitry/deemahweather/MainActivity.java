    class DownloadWeather extends AsyncTask < String, Void, String > {
        @Override
        protected void onPreExecute() {
            super.onPreExecute();

        }
        protected String doInBackground(String...args) {
            String xml = Function.excuteGet("http://api.openweathermap.org/data/2.5/weather?q=" + args[0] +
                    "&units=metric&lang=ru&appid=" + OPEN_WEATHER_MAP_API);
            return xml;
        }
        @Override
        protected void onPostExecute(String xml) {

            try {
                JSONObject json = new JSONObject(xml);
                if (json != null) {
                    JSONObject details = json.getJSONArray("weather").getJSONObject(0);
                    JSONObject main = json.getJSONObject("main");
                    DateFormat df = DateFormat.getDateTimeInstance();

                    cityField.setText(json.getString("name").toUpperCase(Locale.US) + ", " + json.getJSONObject("sys").getString("country"));
                    detailsField.setText(details.getString("description").toUpperCase(Locale.US));
                    currentTemperatureField.setText(String.format("%.2f", main.getDouble("temp")) + "°");
                    humidity_field.setText("Влажность: " + main.getString("humidity") + "%");
                    pressure_field.setText("Давление: " + main.getString("pressure") + " hPa");
                    updatedField.setText(df.format(new Date(json.getLong("dt") * 1000)));
                    weatherIcon.setText(Html.fromHtml(Function.setWeatherIcon(details.getInt("id"),
                            json.getJSONObject("sys").getLong("sunrise") * 1000,
                            json.getJSONObject("sys").getLong("sunset") * 1000)));
                    Date date = new java.util.Date(json.getJSONObject("sys").getLong("sunset")*1000L);
                    SimpleDateFormat sdf = new java.text.SimpleDateFormat("HH:mm");
                    sdf.setTimeZone(java.util.TimeZone.getDefault());
                    String formattedDate = sdf.format(date);
                    sunset_field.setText("Закат: " + formattedDate);
                    date = new java.util.Date(json.getJSONObject("sys").getLong("sunrise")*1000L);
                    sdf = new java.text.SimpleDateFormat("HH:mm");
                    sdf.setTimeZone(java.util.TimeZone.getDefault());
                    formattedDate = sdf.format(date);
                    sunrise_field.setText("Рассвет: " + formattedDate);


                }
            } catch (JSONException e) {
                Toast.makeText(getApplicationContext(), "Error, Check City, City - "+city+" Is invalid", Toast.LENGTH_SHORT).show();
            }


        }



    }



